# Obtener archivos
archivos = [f for f in os.listdir(carreras_dir) if f.endswith('.html')]
archivos.sort()

print(f"Corrigiendo {len(archivos)} landings...")

for archivo in archivos:
    filepath = os.path.join(carreras_dir, archivo)
    
    with open(filepath, 'r') as f:
        contenido = f.read()
    
    # Reemplazar emojis por SVG
    for emoji, svg in emoji_map.items():
        contenido = contenido.replace(emoji, svg)
    
    # Guardar
    with open(filepath, 'w') as f:
        f.write(contenido)
    
    print(f"  ✅ {archivo}")

